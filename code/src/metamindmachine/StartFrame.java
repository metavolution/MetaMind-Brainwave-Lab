    private void SequenceSelectorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SequenceSelectorActionPerformed
        String estr = evt.toString();
        String sname=null;
        
        //System.out.println(evt);
        //if(estr.indexOf("modifiers=Button1") == -1) return;
        int start = estr.indexOf("selectedItemReminder=");
        int end = estr.indexOf(']');
        if(start != -1 && end != -1) {
            sname = estr.substring(start+21,estr.length()-1);
            
            //System.out.println(sname);
            for(int i=0;i < SequenceSelector.getItemCount();i++) {
                if(sname.equals(SequenceSelector.getItemAt(i).toString())) {
                    System.out.println("sequence file "+i+" load: "+seqlistfile[i]);
                    Main.sessionmgr.sequence.LoadSequence(seqlistfile[i]);
                    String lengthstr = "Length: "+Main.sessionmgr.sequence.length / 60 +":"+Main.sessionmgr.sequence.length % 60 + " minutes";
                    lengthLabel.setText(lengthstr);
                }
            }            
            
            GraphPanel.repaint();
        }

    }//GEN-LAST:event_SequenceSelectorActionPerformed
    
