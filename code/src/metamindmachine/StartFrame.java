    /** Creates new form StartFrame */
    public StartFrame() {
        initComponents();
        
        setTitle("MetaMindMachine binaural Brainwave Lab v0.1b");
        //setSize(640, 480);
        
        Toolkit toolkit = Toolkit.getDefaultToolkit();  
        Dimension sSize = toolkit.getScreenSize();  
        int x = (sSize.width - getWidth()) / 2;  
        int y = (sSize.height - getHeight()) / 2;  
        setLocation(x, y);          
        
        //fill Combobox with available sequences
        File path = new File(Main.sequencedir);
        File files[] = path.listFiles();
        Arrays.sort(files);
        String namestr=null;
        
        for(int fi=0;fi < files.length;fi++) {
            String fstr = files[fi].toString();
            if(fstr.substring(fstr.length()-5,fstr.length()).contains(".mmms") != false) {
                
                int start = fstr.indexOf('/');
                if(start != -1) {
                    namestr = fstr.substring(start+1, fstr.length()-5);
                }
                start = fstr.indexOf('_');
                if(start != -1) {
                    namestr = fstr.substring(start+1, fstr.length()-5);
                }
                seqlistfile[seqlistnum] = fstr;
                seqlistname[seqlistnum] = namestr;
                //System.out.println(seqlistname[seqlistnum]);
                SequenceSelector.addItem(makeObj(namestr));
                seqlistnum++;

            }
                
        }
        
        
        setVisible(true);
        getContentPane().setBackground(new Color(111,111,111));
        
        
    }
    
